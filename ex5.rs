fn main() {
    let x = 10;
    let y = 3.14;

    println!("Implicit Type Declaration:");
    println!("Value of x: {}", x);
    println!("Value of y: {}", y);

    let a: i32 = 20;
    let b: f64 = 2.718;
    let c: bool = true;

    println!("\nExplicit Type Declaration:");
    println!("Value of a: {}", a);
    println!("Value of b: {}", b);
    println!("Value of c: {}", c);
}
